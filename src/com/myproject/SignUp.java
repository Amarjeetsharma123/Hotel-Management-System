    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username=jTextField2.getText();
        String password=jTextField3.getText();
        String securityQ=String.valueOf(jComboBox1.getSelectedItem());
        String answer=jTextField4.getText();
        try 
        {
            Connection con=ConnectToDb.connect();
           String query="insert into users(U_NAME,U_PASSWORD,SecurityQ,Answer) values(?,?,?,?)";
          PreparedStatement p= con.prepareStatement(query);
          p.setString(1,username);
          p.setString(2,password);
          p.setString(3,securityQ);
           p.setString(4,answer);
           
          int r=p.executeUpdate();
          if(r==0)
          {
              JOptionPane.showMessageDialog(null, "Record Not Saved","Please Check Your Query",2);
          }
          else
          {
              JOptionPane.showMessageDialog(null, "Record Saved","Congrats",1);
              new Login().setVisible(true);
              this.dispose();
        } 
        }
        catch (HeadlessException |  SQLException e) 
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

