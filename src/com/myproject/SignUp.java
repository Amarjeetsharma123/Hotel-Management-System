    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        // TODO add your handling code here:
        if(jTextField2.getText().trim().equalsIgnoreCase("Username")||jTextField2.getText().trim().equalsIgnoreCase(""))
        {
            jTextField2.setText("Username");
            jTextField2.setForeground(new Color(153,153,153));
        }
        try
        {
            Connection con=ConnectToDb.connect();
            String query="select * from users where U_NAME=?";
            PreparedStatement p=con.prepareStatement(query);
            p.setString(1, jTextField2.getText());
            ResultSet set=p.executeQuery();
            if(set.next())
            {
                JOptionPane.showMessageDialog(null,"Username Already Taken","Please Enter Another Username",2);
                jTextField2.setText("Username");
                jTextField2.setForeground(new Color(153,153,153));
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTextField2FocusLost

